    private void GenerarTabla(Object[] Item) {
        modelo = new TableModelNotEdit();
        Calendar c = GregorianCalendar.getInstance();
        c.setTime(Date.valueOf(Item[1].toString())); //Fecha

        Object Cobrador = ObtenerCobrador(Item[13]); //Cobrador

        int Pagos = Integer.valueOf(Item[11].toString()); //No Pagos
        Object[] Row = new Object[5];

        modelo.addColumn("No. Pago");
        modelo.addColumn("Vencimiento");
        modelo.addColumn("Fecha Pago");
        modelo.addColumn("Importe");
        modelo.addColumn("Cobrador");
        Estado.setModel(modelo);

        for (int x = 1; x <= Pagos; x++) {
            if (x > 1) {
                c.add(Calendar.DATE, Pagos);
            }

            Row[0] = x;
            Row[1] = c.get(Calendar.DAY_OF_MONTH) + "/" + c.get(Calendar.MONTH) + "/" + c.get(Calendar.YEAR);
            if (ExisteFactura(x)) {
                Object[] ObtenerFactura = ObtenerFactura(x);
                Row[2] = ObtenerFactura[0];
                Row[3] = ObtenerFactura[1];
            } else {
                Row[2] = "";
                Row[3] = "0";

            }
            Row[4] = Cobrador;
            modelo.addRow(Row);
        }
        ObtenrerSaldo();
    }

    private Object ObtenerCobrador(Object Clave) {
        try {
            ResultSet SqlQuery = Sql.SqlQuery(Sql.connection, "SELECT * FROM clientes");
            Object Row = new Object();
            if (SqlQuery.next()) {
                Row = SqlQuery.getObject(2);
            }
            Sql.Clear();
            return Row;
        } catch (SQLException ex) {
            Log.SaveLog(ex.toString());
        }
        return null;
    }

    private void ObtenrerSaldo() {
        Double Saldo = new Double(0);
        for (int x = 0; x < Estado.getRowCount(); x++) {
            Saldo += Double.valueOf(Estado.getValueAt(x, 3).toString());
        }
        Double Total = Double.valueOf(SaldoInicial.getText()) - Saldo;
        SaldoActual.setText(Total.toString());
    }

    private void Update(Object[] Entry) {
        Calendar c = GregorianCalendar.getInstance();
        try {
            PreparedStatement SqlQueryStmts = null;
            SqlQueryStmts = Sql.SqlQueryStmts(Sql.connection, "INSERT INTO facturas VALUES (?,?,?,?,?)");
            SqlQueryStmts.setNull(1, java.sql.Types.INTEGER);
            SqlQueryStmts.setInt(2, Integer.valueOf(Factura.getText())); //Venta
            SqlQueryStmts.setInt(3, Integer.valueOf(Entry[0].toString())); //Pago
            SqlQueryStmts.setDate(4, new Date(c.getTime().getTime())); //Fecha
            SqlQueryStmts.setDouble(5, Double.valueOf(Entry[3].toString())); //Importe
            SqlQueryStmts.execute();
            SqlQueryStmts.close();
            Sql.Clear();
            JOptionPane.showMessageDialog(this, "Registro Insertado Correctamente", "SQL INFO", JOptionPane.INFORMATION_MESSAGE, null);
            GenerarTabla(Tabla);
        } catch (SQLException ex) {
            Log.SaveLog(ex.toString());
            JOptionPane.showMessageDialog(this, "Ocurrio un error", "SQL ERROR", JOptionPane.ERROR_MESSAGE, null);
        }
    }

    private boolean ExisteFactura(int Pago) {
        try {
            ResultSet SqlQuery = Sql.SqlQuery(Sql.connection, "SELECT * FROM facturas WHERE venta=" + Factura.getText() + " AND no_pago=" + Pago);
            if (SqlQuery.next()) {
                Sql.Clear();
                return true;
            }
        } catch (SQLException ex) {
            Log.SaveLog(ex.toString());
        }
        return false;
    }

    private Object[] ObtenerFactura(int Pago) {
        try {
            ResultSet SqlQuery = Sql.SqlQuery(Sql.connection, "SELECT fecha,importe FROM facturas WHERE venta=" + Factura.getText() + " AND no_pago=" + Pago);
            ResultSetMetaData metaData = SqlQuery.getMetaData();
            Object[] Row = new Object[2];
            if (SqlQuery.next()) {
                for (int x = 0; x < metaData.getColumnCount(); x++) {
                    Row[x] = SqlQuery.getObject(x + 1);
                }
                Sql.Clear();
                return Row;
            }
        } catch (SQLException ex) {
            Log.SaveLog(ex.toString());
        }
        return null;
    }

