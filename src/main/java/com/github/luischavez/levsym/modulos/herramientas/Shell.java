    private void jTextField2KeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jTextField2KeyReleased
        // TODO add your handling code here:
        //Si presiona Enter
        if (evt.getKeyCode() == KeyEvent.VK_ENTER) {
            try {
                Boolean state = Instruccion.execute(jTextField2.getText());
                ResultSet resultSet = Instruccion.getResultSet();
                ResultSetMetaData metaData = null;
                int[] SizeColumns = null;
                if (state) {
                    metaData = resultSet.getMetaData();
                    SizeColumns = new int[metaData.getColumnCount()];
                }
                int ColumnLength = 0;

                if (!(resultSet == null)) { //Si no es nulo
                    while (resultSet.next()) {
                        for (int x = 0; x < metaData.getColumnCount(); x++) {
                            ColumnLength = metaData.getColumnName(x + 1).length();
                            if (SizeColumns[x] < ColumnLength) {
                                SizeColumns[x] = ColumnLength;
                            }
                            ColumnLength = resultSet.getObject(x + 1).toString().length();
                            if (SizeColumns[x] < ColumnLength) {
                                SizeColumns[x] = ColumnLength;
                            }
                        }
                    }
                    resultSet.beforeFirst();
                    for (int x = 0; x < metaData.getColumnCount(); x++) {
                        for (int z = 0; z < SizeColumns[x] + 2; z++) {
                            jTextArea1.append("_");
                        }
                    }
                    jTextArea1.append("\n");
                    for (int x = 0; x < metaData.getColumnCount(); x++) {
                        jTextArea1.append("|" + metaData.getColumnName(x + 1));
                        for (int y = 0; y < SizeColumns[x] - metaData.getColumnName(x + 1).length(); y++) {
                            jTextArea1.append(" ");
                        }
                        jTextArea1.append("|");
                    }
                    jTextArea1.append("\n");
                    while (resultSet.next()) { //Obtiene el siguiente resultado
                        //Recorre las columnas del resultado
                        for (int x = 0; x < metaData.getColumnCount(); x++) {
                            //Muestra el resultado por pantalla
                            jTextArea1.append("|" + resultSet.getObject(x + 1).toString());
                            for (int y = 0; y < SizeColumns[x] - resultSet.getObject(x + 1).toString().length(); y++) {
                                jTextArea1.append(" ");
                            }
                            jTextArea1.append("|");
                        }
                        jTextArea1.append("\n");
                        for (int x = 0; x < metaData.getColumnCount(); x++) {
                            for (int z = 0; z < SizeColumns[x] + 2; z++) {
                                jTextArea1.append("-");
                            }
                        }
                        jTextArea1.append("\n");
                    }
                } else {
                    //Muestra el estado de la ejecucion
                    jTextArea1.append("Execute: " + !state + "\n");
                }
            } catch (SQLException ex) {
                //Muestra el error
                jTextArea1.append("Warning: " + ex + "\n");
                Log.SaveLog(ex.toString());
            }
        }
    }//GEN-LAST:event_jTextField2KeyReleased


