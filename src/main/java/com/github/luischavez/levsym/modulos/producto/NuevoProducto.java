    private void Update(Object[] Entry) {
        try {
            PreparedStatement SqlQueryStmts = null;
            switch (Operacion) {
                case 0:
                    SqlQueryStmts = Sql.SqlQueryStmts(Sql.connection, "INSERT INTO productos VALUES (?,?,?,?,?,?,?,?,?,?,?,?,?,?)");
                    SqlQueryStmts.setNull(1, java.sql.Types.INTEGER);
                    break;
                case 1:
                    SqlQueryStmts = Sql.SqlQueryStmts(Sql.connection, "UPDATE productos SET clave = ? "
                            + ", nombre = ? "
                            + ", codigo_ean = ? "
                            + ", iva = ? "
                            + ", descripcion = ? "
                            + ", tipo_almacen = ? "
                            + ", departamento = ? "
                            + ", categoria = ? "
                            + ", proveedor = ? "
                            + ", existencia = ? "
                            + ", total = ? "
                            + ", costo_unit = ? "
                            + ", precio_venta = ? "
                            + ", observaciones = ? WHERE clave=" + t.getTitle());
                    SqlQueryStmts.setInt(1, Integer.valueOf(t.getTitle())); //Clave
                    break;
            }
            SqlQueryStmts.setString(2, Entry[0].toString()); //Nombre
            SqlQueryStmts.setDouble(3, Double.valueOf(Entry[1].toString())); //Codigo de barras
            SqlQueryStmts.setDouble(4, Double.valueOf(Entry[2].toString())); //IVA
            SqlQueryStmts.setString(5, Entry[3].toString()); //Descripcion
            SqlQueryStmts.setInt(6, Integer.valueOf(Entry[4].toString())); //Almacen
            SqlQueryStmts.setInt(7, Integer.valueOf(Entry[5].toString())); //Departamento
            SqlQueryStmts.setInt(8, Integer.valueOf(Entry[6].toString())); //Categoria
            SqlQueryStmts.setInt(9, Integer.valueOf(Entry[7].toString())); //Proveedor
            SqlQueryStmts.setInt(10, Integer.valueOf(Entry[8].toString())); //Existencia
            SqlQueryStmts.setInt(11, Integer.valueOf(Entry[9].toString())); //Total
            SqlQueryStmts.setDouble(12, Double.valueOf(Entry[10].toString())); //Costo por unidad
            SqlQueryStmts.setDouble(13, Double.valueOf(Entry[11].toString())); //Precio publico
            SqlQueryStmts.setString(14, Entry[12].toString()); //Observaciones
            SqlQueryStmts.execute();
            ResultSet generatedKeys = SqlQueryStmts.getGeneratedKeys();
            if (generatedKeys.next()) {
                t = (TitledBorder) jPanel1.getBorder();
                t.setTitle(Calc.ZeroFill("00000", generatedKeys.getObject(1).toString()));
            }
            SqlQueryStmts.close();
            Sql.Clear();
            JOptionPane.showMessageDialog(this, "Registro Insertado Correctamente", "SQL INFO", JOptionPane.INFORMATION_MESSAGE, null);
        } catch (SQLException ex) {
            Log.SaveLog(ex.toString());
            JOptionPane.showMessageDialog(this, "Ocurrio un error", "SQL ERROR", JOptionPane.ERROR_MESSAGE, null);
        }
    }

