    private void GenerarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_GenerarActionPerformed
        // TODO add your handling code here:
        //Nuevo PDF
        pdf = new PDF();
        try {
            switch (Selected) {
                case 0:
                    if (jCheckBox1.isSelected()) {
                        ResultSet SqlQuery = Sql.SqlQuery(Sql.connection, "SELECT * FROM productos");
                        pdf.GeneraPDF(SqlQuery);
                    }
                    if (jCheckBox2.isSelected()) {
                        ResultSet SqlQuery = Sql.SqlQuery(Sql.connection, "SELECT * FROM proveedor");
                        pdf.GeneraPDF(SqlQuery);
                    }
                    if (jCheckBox3.isSelected()) {
                        ResultSet SqlQuery = Sql.SqlQuery(Sql.connection, "SELECT * FROM departamento");
                        pdf.GeneraPDF(SqlQuery);
                    }
                    if (jCheckBox4.isSelected()) {
                        ResultSet SqlQuery = Sql.SqlQuery(Sql.connection, "SELECT * FROM categoria");
                        pdf.GeneraPDF(SqlQuery);
                    }
                    break;
                case 1:
                    ResultSet SqlQuery = Sql.SqlQuery(Sql.connection, "SHOW TABLES");
                    while (SqlQuery.next()) {
                        ResultSet SqlQuery1 = Sql.SqlQuery(Sql.connection, "SELECT * FROM " + SqlQuery.getString(1));
                        pdf.GeneraPDF(SqlQuery1);
                    }
                    break;
                case 2:
                    pdf.CreateTablePDF(jTable1);
                    break;
            }
        } catch (Exception ex) {
            Log.SaveLog(ex.toString());
        }
    }//GEN-LAST:event_GenerarActionPerformed

